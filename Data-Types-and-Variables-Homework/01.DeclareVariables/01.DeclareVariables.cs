using System;

    class DeclareVariables
    {
        static void Main()
        {
            // Definining variables of different types according to the assigned values
            ushort ushortVariable = 52130;
            sbyte sbyteVariable = -115;
            int intVariable = 4825932;
            byte byteVariable = 97;
            short shortVariable = -10000;

            //Printing the sequence on the console
            Console.WriteLine("{0}, {1}, {2}, {3}, {4}", ushortVariable, sbyteVariable, intVariable, byteVariable, shortVariable);

        }
    }
